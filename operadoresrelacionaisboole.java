package meuprojeto;

public class operadoresrelacionaisboole {

	public static void main(String[] args) {
		int x = 10;
		int k = 20;
		System.out.printf("x é igual a 10? %b\n", x == 10);
		System.out.printf("x é diferente de 10 %b\n", x != 10);
		System.out.printf("x é maior que k? %b\n", x > k);
		System.out.printf("x é maior ouo igual a 10? %b \n", x >= 10);
		System.out.printf("x é menor que 10? %b\n", x < 10);
		System.out.printf("x é menor ou igual a k? %b", x <= k);
		
/*
 * Exercício dois com operador condicional ternário ?
 */
	}


}
